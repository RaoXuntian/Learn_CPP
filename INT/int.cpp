#include <iostream>

using namespace std;

int main() {
	cout << "INT_MIN = " << INT_MIN << endl;
	cout << "INT_MIN/10 = " << INT_MIN/10 << endl;
	cout << "INT_MIN%10 = " << INT_MIN%10 << endl;
	cout << "-INT_MIN%10 = " << -INT_MIN%10 << endl;
	cout << "-(INT_MIN%10) = " << -(INT_MIN%10) << endl;
	return 0;
}
